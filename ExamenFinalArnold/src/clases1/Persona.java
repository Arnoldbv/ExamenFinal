package clases1;


public class Persona {
    private String nombreCompleto;
    private int edad;
    
    public Persona(String nombreCompleto, int edad) {
        this.nombreCompleto = nombreCompleto;
        this.edad = edad;
        
    }

    public String getNombreCompleto() {
        return nombreCompleto;
    }

    public int getEdad() {
        return edad;
    }
    
    
    
}




